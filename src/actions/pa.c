#include "push_swap.h"

void	pa(t_ps *ps)
{
	if (ps->b->size < 1)
		return ;
	ft_stackpush(ps->a, ft_stackpop(ps->b));
	if (PRINT_MOVES)
		ft_putstr_fd("pa\n", 1);
}
